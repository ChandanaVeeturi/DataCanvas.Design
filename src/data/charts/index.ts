export function getChartsByCategory(category: ChartCategory): ChartDef[] {
  return CHARTS.filter((c) => c.category === category)
}

export function getChartBySlug(slug: string): ChartDef | undefined {
  return CHARTS.find((c) => c.id === slug)
}

export function getCategoryBySlug(slug: string): CategoryDef | undefined {
  return CATEGORIES.find((c) => c.id === slug)
}

export function getAllChartSlugs(): { category: string; slug: string }[] {
  return CHARTS.map((c) => ({ category: c.category, slug: c.id }))
}
